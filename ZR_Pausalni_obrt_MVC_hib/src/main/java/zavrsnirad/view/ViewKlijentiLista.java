private void ucitaj() {
        DefaultListModel<Klijent_kupac> model = new DefaultListModel<>();
        obrada.getEntiteti().forEach(
                (klijent_kupac) -> {
                    model.addElement(klijent_kupac);
                });

        lista.setModel(model);
        lista.repaint();
    }
